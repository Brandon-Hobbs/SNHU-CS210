/*
Description:
	This function requires no passed parameters and returns no value. It will open the hardcoded frequency.txt file
	and create a text based histogram
Example:
	printHistogram();
Output:
	A histogram is written to the console
Return:
	None
*/
void printHistogram() 
{
	string READ_FILE = "U:\\CS-210-Python-C++\\frequency.dat";
	ifstream inFile;
	string lineRead;

	//open the file and then check that it truly is open
	inFile.open(READ_FILE);
	if (!inFile.is_open()) {
		cout << "Could not open the file." << endl;
	}

	//process the file until EOF
	while (!inFile.eof()) {
		inFile >> lineRead;
		cout << lineRead;

		inFile >> lineRead;

		//handle the case of stray carriage returns
		if (lineRead != "") {
			for (int i = 1; i <= stoi(lineRead); i++) { //convert the string to an int and loop
				cout << "#";
			}

			cout << endl;
		}

		//flush lineRead
		lineRead = "";
	}
	//close the file
	inFile.close();
}

